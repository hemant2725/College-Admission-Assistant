def detect_phd_intent(message: str):
    """
    Detect PhD-specific intent with confidence score.
    Returns (intent, confidence) or (None, 0.0) if not PhD-related.
    """
    import re
    text = message.lower()
    text = re.sub(r"p[\.\-\s]*h[\.\-\s]*d[\.]?", "phd", text)
    cleaned = re.sub(r"[^\w\s]", " ", text)
    msg_norm = " " + " ".join(cleaned.split()) + " "

    def _contains_phrase(phrase: str) -> bool:
        phrase_norm = " ".join(phrase.lower().split())
        if not phrase_norm:
            return False
        return f" {phrase_norm} " in msg_norm

    scores = {}

    for intent, keywords in PhD_INTENT_KEYWORDS.items():
        score = sum(1 for kw in keywords if _contains_phrase(kw))
        if score > 0:
            scores[intent] = score

    if not scores:
        return None, 0.0

    best_intent = max(scores, key=scores.get)
    confidence = min(scores[best_intent] / 2.0, 1.0)
    return best_intent, confidence
