def detect_mtech_intent(message: str):
    """
    Detect M.Tech.-specific intent with confidence score.
    Returns (intent, confidence) or (None, 0.0) if not M.Tech.-related.
    """
    import re
    # Normalise degree abbreviations before stripping punctuation so that
    # "M.Tech.", "M. Tech", "M-Tech" all become "mtech"
    text = message.lower()
    text = re.sub(r"m[\.\-\s]*tech[\.]?", "mtech", text)
    text = re.sub(r"b[\.\-\s]*tech[\.]?", "btech", text)
    text = re.sub(r"m[\.\-\s]*sc[\.]?",   "msc",   text)
    text = re.sub(r"m[\.\-\s]*ca[\.]?",   "mca",   text)
    text = re.sub(r"m[\.\-\s]*e[\.]?",    "me",    text)
    text = re.sub(r"p[\.\-\s]*e[\&\s]*c", "pec",   text)
    text = re.sub(r"sp[\&\s]*ml",          "spml",  text)
    text = re.sub(r"e[\&\s]*e",            "ee",    text)
    cleaned = re.sub(r"[^\w\s]", " ", text)
    msg_norm = " " + " ".join(cleaned.split()) + " "

    def _contains_phrase(phrase: str) -> bool:
        phrase_norm = " ".join(phrase.lower().split())
        if not phrase_norm:
            return False
        return f" {phrase_norm} " in msg_norm

    scores = {}
    for intent, keywords in MTECH_INTENT_KEYWORDS.items():
        score = sum(1 for kw in keywords if _contains_phrase(kw))
        if score > 0:
            scores[intent] = score

    if not scores:
        return None, 0.0

    best_intent = max(scores, key=scores.get)
    confidence = min(scores[best_intent] / 2.0, 1.0)
    return best_intent, confidence


